func TestRefundPayment_success(t *testing.T) {
	var capturedBody map[string]any

	_, client := newTestServerFn(t, func(w http.ResponseWriter, r *http.Request) {
		_ = json.NewDecoder(r.Body).Decode(&capturedBody)
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)
		_ = json.NewEncoder(w).Encode(Payment{
			PaymentID: "pay_refunded",
			Status:    "refunded",
			Amount:    Money{Amount: "50000.00", Currency: "UZS"},
		})
	})

	got, err := client.RefundPayment(context.Background(), "pay_refunded", RefundRequest{
		Amount: Money{Amount: "50000.00", Currency: "UZS"},
		Reason: "customer_request",
	})
	if err != nil {
		t.Fatalf("RefundPayment returned error: %v", err)
	}
	if got.Status != "refunded" {
		t.Errorf("expected status refunded, got %q", got.Status)
	}
	if capturedBody["reason"] != "customer_request" {
		t.Errorf("expected reason in body, got %v", capturedBody["reason"])
	}
}

