// newTestServer creates an httptest.Server that responds with the given status
// and JSON body for any request. Returns the server and a client pointed at it.
func newTestServer(t *testing.T, status int, body any) (*httptest.Server, *Client) {
	t.Helper()
	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(status)
		if body != nil {
			_ = json.NewEncoder(w).Encode(body)
		}
	}))
	t.Cleanup(srv.Close)
	return srv, New(srv.URL, "mg_test_key")
}

// newTestServerFn creates a server with a custom handler function.
func newTestServerFn(t *testing.T, fn http.HandlerFunc) (*httptest.Server, *Client) {
	t.Helper()
	srv := httptest.NewServer(fn)
	t.Cleanup(srv.Close)
	return srv, New(srv.URL, "mg_test_key")
}

func TestCreatePayment_success(t *testing.T) {
	want := Payment{
		PaymentID: "pay_abc123",
		Status:    "pending",
		Amount:    Money{Amount: "150000.00", Currency: "UZS"},
		OrderID:   "order_001",
	}

	_, client := newTestServer(t, http.StatusCreated, want)

	got, err := client.CreatePayment(context.Background(), CreatePaymentRequest{
		Amount:  Money{Amount: "150000.00", Currency: "UZS"},
		OrderID: "order_001",
	})
	if err != nil {
		t.Fatalf("CreatePayment returned error: %v", err)
	}
	if got.PaymentID != want.PaymentID {
		t.Errorf("got PaymentID %q, want %q", got.PaymentID, want.PaymentID)
	}
	if got.Status != want.Status {
		t.Errorf("got Status %q, want %q", got.Status, want.Status)
	}
}

func TestClientUsesAPIKeyHeader(t *testing.T) {
	var apiKey string
	var authorization string

	_, client := newTestServerFn(t, func(w http.ResponseWriter, r *http.Request) {
		apiKey = r.Header.Get("X-API-Key")
		authorization = r.Header.Get("Authorization")
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusCreated)
		_ = json.NewEncoder(w).Encode(Payment{PaymentID: "pay_header", Status: "pending"})
	})

	_, err := client.CreatePayment(context.Background(), CreatePaymentRequest{
		Amount:  Money{Amount: "150000.00", Currency: "UZS"},
		OrderID: "order_header",
	})
	if err != nil {
		t.Fatalf("CreatePayment returned error: %v", err)
	}
	if apiKey != "mg_test_key" {
		t.Fatalf("expected X-API-Key mg_test_key, got %q", apiKey)
	}
	if authorization != "" {
		t.Fatalf("expected no Authorization header for API key auth, got %q", authorization)
	}
}

