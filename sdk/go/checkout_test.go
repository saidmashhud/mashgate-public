func TestCreateCheckout_success(t *testing.T) {
	want := CheckoutSession{
		SessionID:   "cs_xyz789",
		Status:      "pending",
		CheckoutURL: "https://pay.mashgate.uz/cs_xyz789",
		TotalAmount: Money{Amount: "2500000.00", Currency: "UZS"},
		SuccessURL:  "https://zist.uz/success",
		CancelURL:   "https://zist.uz/cancel",
	}

	_, client := newTestServer(t, http.StatusCreated, map[string]any{
		"session":     want,
		"checkoutUrl": want.CheckoutURL,
	})

	got, err := client.CreateCheckout(context.Background(), CreateCheckoutRequest{
		TotalAmount:   Money{Amount: "2500000.00", Currency: "UZS"},
		SuccessURL:    "https://zist.uz/success",
		CancelURL:     "https://zist.uz/cancel",
		CustomerEmail: "guest@example.com",
		Items: []LineItem{
			{
				Name:      "Apartment in Tashkent",
				Quantity:  3,
				UnitPrice: Money{Amount: "833333.33", Currency: "UZS"},
			},
		},
	})
	if err != nil {
		t.Fatalf("CreateCheckout error: %v", err)
	}
	if got.SessionID != want.SessionID {
		t.Errorf("expected SessionID %q, got %q", want.SessionID, got.SessionID)
	}
	if got.CheckoutURL != want.CheckoutURL {
		t.Errorf("expected CheckoutURL %q, got %q", want.CheckoutURL, got.CheckoutURL)
	}
}

func TestCreateCheckout_idempotencyAutoGenerated(t *testing.T) {
	var capturedKey string

	_, client := newTestServerFn(t, func(w http.ResponseWriter, r *http.Request) {
		capturedKey = r.Header.Get("Idempotency-Key")
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusCreated)
		_ = json.NewEncoder(w).Encode(map[string]any{
			"session": CheckoutSession{SessionID: "cs_auto"},
		})
	})

	_, err := client.CreateCheckout(context.Background(), CreateCheckoutRequest{
		TotalAmount: Money{Amount: "100000.00", Currency: "UZS"},
		SuccessURL:  "https://zist.uz/success",
		CancelURL:   "https://zist.uz/cancel",
	})
	if err != nil {
		t.Fatalf("CreateCheckout error: %v", err)
	}
	if capturedKey == "" {
		t.Fatal("expected Idempotency-Key header to be set, got empty string")
	}
}

