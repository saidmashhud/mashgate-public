func TestCompleteCheckout_wallet(t *testing.T) {
	wantRedirect := "https://my.click.uz/pay?txn=12345"

	var capturedBody map[string]any

	_, client := newTestServerFn(t, func(w http.ResponseWriter, r *http.Request) {
		_ = json.NewDecoder(r.Body).Decode(&capturedBody)
		w.Header().Set("Content-Type", "application/json")
		w.WriteHeader(http.StatusOK)
		_ = json.NewEncoder(w).Encode(CompleteCheckoutResponse{
			Success:           true,
			PaymentID:         "pay_wallet_123",
			WalletRedirectURL: wantRedirect,
		})
	})

	got, err := client.CompleteCheckout(context.Background(), "cs_xyz789", CompleteCheckoutRequest{
		PaymentMethodType: "wallet",
		Wallet: &WalletPaymentMethod{
			Provider: "click",
			Phone:    "+998901234567",
		},
	})
	if err != nil {
		t.Fatalf("CompleteCheckout error: %v", err)
	}
	if !got.Success {
		t.Error("expected Success=true")
	}
	if got.WalletRedirectURL != wantRedirect {
		t.Errorf("expected WalletRedirectURL %q, got %q", wantRedirect, got.WalletRedirectURL)
	}

	// Verify the request body contained the wallet provider
	if wallet, ok := capturedBody["wallet"].(map[string]any); !ok {
		t.Error("expected wallet field in request body")
	} else if wallet["provider"] != "click" {
		t.Errorf("expected provider=click, got %v", wallet["provider"])
	}
}

